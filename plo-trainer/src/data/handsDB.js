const shuffle = (arr) => [...arr].sort(() => Math.random() - 0.5);

export function generateCards(template) {
  const SUITS = ['s', 'h', 'd', 'c'];
  const { ranks, suitedness } = template;
  const shuffledSuits = shuffle([...SUITS]);
  let suits;

  if (suitedness === 'ds') {
    const [s1, s2] = shuffledSuits;
    suits = shuffle([s1, s2, s1, s2]);
  } else if (suitedness === 'ss') {
    const [s1, s2, s3] = shuffledSuits;
    suits = shuffle([s1, s1, s2, s3]);
  } else {
    suits = shuffle([...SUITS]);
  }

  return ranks.map((rank, i) => ({ rank, suit: suits[i] }));
}

